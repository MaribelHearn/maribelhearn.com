        <td>Huge range of homing. Good bombs. Easier stages. Very straightforward to use. 4 bombs per life.</td>
        <td>Somewhat poor shot damage. Very low Cherry gain, which means less borders. Very fast focused movement speed, which can make dodging more difficult.</td>
    </tr>
    <tr>
        <th>SakuyaB<br>(Angled)</th>
        <td>Angling shot, forward focus with knives that can be angled when unfocused and locked when focused.</td>
        <td>Strong bomb with long invincibility, despite disabling your shot. Also clears the screen. 4 bombs per life. High shot damage.</td>
        <td>Bombs do almost no damage. Very fast focused movement speed, which can make dodging more difficult. Shot angling can be tricky to use.</td>
    </tr>
</table>
