        <td>Gets a bomb when clearing a stage if you have less than 3 bombs left.</td>
        <td>Focused shot spread is very good against stages. High unfocused shot damage.</td>
        <td>Focused shot is bad against bosses. Unfocused shot can be difficult to angle correctly.</td>
    </tr>
