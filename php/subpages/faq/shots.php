        <td>Shieldbomb that cancels a hit while active, gets 0.5 power back when the bomb expires if you were not hit.</td>
        <td>Bomb is quite good for Extra, as bombs do not damage the boss's Spell Cards.</td>
        <td>Hard Stage 4 boss. Lagging shots often severely decrease shot damage. Bomb does no damage and does not clear bullets when hit.</td>
