<h2 id='signup'>Sign-up</h2>
<img class='guide' src='../assets/other/thvote/signup.jpg' alt='Sign-up options'>
<p>You can sign up for the Touhou Popularity Poll by clicking "Issue Voting ID". You will then fill in your email in the first textbox, and optionally your previous voting ID in the second textbox (see below).</p>
<p>Of course, you can also sign up with Google or Twitter if you so desire.</p>
<img class='guide' src='../assets/other/thvote/id.jpg' alt='Email form'>
<p>If you used the "Issue Voting ID" method, it might take some time, but you should receive an email containing your voting ID. You will use this voting ID to fill in your votes.</p>
<h2 id='voting'>Voting</h2>
<p>After issuing your ID, click on "Vote Now" either at the bottom of the main page, or in the navigation menu on the left.<br>Choose your category from the big buttons to start voting!</p>
<img class='guide_mobile' src='../assets/other/thvote/voting.jpg' alt='Voting form'>
<p>Each voting form will look like this. The first textbox is for your voting ID. The second one is, optionally, your nickname.</p>
<p>The characters, music and works are <strong>in English</strong> as of this year! This makes voting much easier. Everything is chronologically ordered.</p>
<img class='guide_mobile' src='../assets/other/thvote/voting_english.png' alt='English voting'>
<p>The first one gets 2 points, all others get 1 point. The second textbox for each item is optional; you can fill in additional comments there.</p>
<p>For the questionnaire, there is a translation of the 2023 iteration  on the main page on this site: <a href='/thvote#questionnaire'>https://maribelhearn.com/thvote#questionnaire</a>.</p>
<p>At the bottom, there is a checkbox. If you check it, you agree to potentially having your comments featured on the <strong>Touhou Station stream</strong>. Leave it unchecked if you do not want to be part of it.</p>
<h2 id='fanworks'>Submitting supporting fanworks</h2>
<p>To submit a supporting fanwork for a character, navigate to "Support Works" using the navigation menu on the left.</p>
<img src='../assets/other/thvote/support_works.jpg' alt='Support Works menu item'>
<p>On this page, scroll to the very bottom, where you will find the submission form. See below for English translations of the page and the form.</p>
<h3>What are support works?</h3>					
<p>(Translation) Here's a support link where people can freely register their works.							
Please feel free to register URLs of illustrations, videos, and other works to show support for your favourite characters.							
Liven up the popularity poll with your support!</p>
<p>We will begin accepting submissions from the 10th of September.</p>
<h3>Before you submit</h3>
<p>Please only submit your own original work.</p>
<h3>Submission Rules</h3>
<ul>
    <li>You can submit as many works as you want in any format; manga, fanfic, music, video, etc.;</li>
    <li>You can only submit your own work;</li>							
    <li>Please only submit completed works. Refrain from submitting unfinished works;</li>
    <li>Please submit works that were made after the previous poll (September of 2023);</li>
    <li>While there are no limitations on word count, please refrain from submitting a simple support comment;</li>
    <li>Please refrain from submitting works with requests such as "I'll do 〇〇〇 (If this character gets within 〇〇th place / if you vote for 〇〇〇)";</li>
    <li>We do not provide a platform to upload your works. Please upload the works on your own website or a different website and then submit a link to that work;</li>
    <li>Please refrain from uploading works that contain other people's works that were used withou permission (such as AMVs, etc.);</li>
    <li>Works deemed inappropriate may be deleted without prior notice;</li>
    <li>We might edit the tags a work is submitted with;</li>
    <li>The rights of submitted works belong to their respective creators.</li>
</ul>
<img class='guide_mobile' src='../assets/other/thvote/support_form.png' alt='Support Works submission form'>
<p>At the bottom of the form, there is a checkbox. If you check it, you agree to potentially having your work featured on the <strong>Touhou Station stream</strong>. Leave it unchecked if you do not want to be part of it.
See below for a translation of the surrounding text.</p>
<img class='guide_mobile' src='../assets/other/thvote/touhou_station.jpg' alt='Touhou Station checkbox'>
<h3>Regarding featuring your work on Touhou Station</h3>
<p>(Translation) We are planning to feature some of the submitted works during the Touhou Station broadcast planned for a later date.
If you are free to having your work featured on stream, please check the box below.</p>
