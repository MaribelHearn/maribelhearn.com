    <h2>How to Submit</h2>
    <p>Explanation of the submission form linked above, copied from the TRS Discord server.</p>
    <h3>Player name</h3>
    <p>Your username. The name we'll display when your run will be shown.</p>
    <h3>Replay Link</h3>
    <p>The link to your replay. For more information on where and how to upload your replay please check the section below regarding how to upload your replay.
    If your run is a fangame run or was made using specific patches please provide a YouTube link.</p>
    <h3>Game</h3>
    <p>Just check the game you played during the run. We've listed all the official Touhou games.
    It is important to note that Seihou and fangame runs are also accepted, in this case please write down the game name in the "Other" section.</p>
    <h3>Difficulty</h3>
    <p>You need to select the difficulty you used during your run.
    If you used a custom difficulty please write the name in the "Other" section.</p>
    <h3>Shottype / Routes</h3>
    <p>Write down the shot type you used in the run, for example ReimuA, MarisaWolf or AyaSpring.
    If your run is a GFW run please write the route you chose instead, for example A1.</p>
    <h3>Type of run</h3>
    <p>Select the type of your run.</p>
    <h3>Special condition</h3>
    <p>Check every special condition that applies to your run.
    If your condition doesn't appear in the list please specify it in the "Other" section.</p>
    <h3>What section would you like to skip?</h3>
    <p>By default for survival runs we will fast forward everything from stage 1 to the stage 3 boss.
    Here you can specify other settings if you want, you can even ask for your run to be shown fully but be sure to have a good reason for it.
    Scoring runs are always shown fully by default, you can also ask us to fast forward some sections if you like.</p>
    <h3>Comments about the run?</h3>
